SELECT I.COD_EMPL,MAX(E.NOM_EMPL)||' '||MAX(E.APE_EMPL) NOMBRE,MAX(I.TIP_INCA)TIP_INCA,MAX(I.NUM_DIAT)NUM_DIAT,MIN(I.FEC_DESD)FEC_DESD,
MAX(I.FEC_HAST)FEC_HAST,I.COD_DIAR,MAX(S.NOM_DIAG) NOM_DIAG,MAX(C.RMT_PLAN) ID_PLAN,MAX(L.NOM_CARE) NOM_CARE,(T.NOM_CCOS) NOM_CCOS,MAX(I.NUM_INPR)NUM_INPR,      
CASE
WHEN MAX(I.IND_PRGA) = 'N' THEN 'I'
WHEN MAX(I.IND_PRGA) = 'S' THEN 'P'
ELSE ' ' END INDPRO,MAX(D.NOM_ENTI) EPS, MAX(O.NOM_ENTI) AFP                                                      
FROM NM_INCAP I
INNER JOIN BI_EMPLE E ON I.COD_EMPR = E.COD_EMPR AND I.COD_EMPL = E.COD_EMPL 
INNER JOIN SO_CODIA S ON I.COD_EMPR = S.COD_EMPR AND I.COD_DIAR = S.COD_DIAG 
INNER JOIN NM_CONTR C ON I.COD_EMPR = C.COD_EMPR AND I.COD_EMPL = C.COD_EMPL
INNER JOIN NM_CAREL L ON I.COD_EMPR = L.COD_EMPR AND C.COD_CARG = L.COD_CARE 
INNER JOIN GN_CCOST T ON I.COD_EMPR = T.COD_EMPR AND C.COD_area = T.COD_CCOS  
INNER JOIN NM_CUENT U ON I.COD_EMPR = U.COD_EMPR AND I.COD_EMPL = U.COD_EMPL AND I.NRO_CONT = U.NRO_CONT AND U.TIP_ENTI='EPS' 
INNER JOIN NM_ENTID D ON I.COD_EMPR = D.COD_EMPR AND U.TIP_ENTI = D.TIP_ENTI AND U.COD_ENTI = D.COD_ENTI AND D.COD_SUCU=0
INNER JOIN NM_CUENT P ON I.COD_EMPR = P.COD_EMPR AND I.COD_EMPL = P.COD_EMPL AND I.NRO_CONT = P.NRO_CONT AND P.TIP_ENTI='AFP' 
INNER JOIN NM_ENTID O ON I.COD_EMPR = O.COD_EMPR AND P.TIP_ENTI = O.TIP_ENTI AND P.COD_ENTI = O.COD_ENTI AND O.COD_SUCU=0
WHERE
C.IND_ACTI = 'A' AND
I.COD_EMPR = 311  AND
I.FEC_HAST >= '01/11/2022' AND -- 6 MESES HACIA ATRAS
I.NUM_DIAT > 180  AND
I.NUM_DIAT <= 5555 AND   
I.TIP_INCA = 'IGE'
GROUP BY T.NOM_CCOS,I.COD_EMPL,I.COD_CONC,I.COD_DIAR 
ORDER BY T.NOM_CCOS,I.COD_EMPL
