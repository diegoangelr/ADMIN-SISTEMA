SELECT C.COD_EMPL , C.NRO_CONT, C.IND_ACTI ,C.FOR_PAGO ,N.COD_ENTI, N.CLA_CUEN, N.COD_SUCU
FROM NM_CONTR C
LEFT JOIN NM_CUENT N ON C.COD_EMPL = N.COD_EMPL AND C.NRO_CONT = N.NRO_CONT
WHERE ((FOR_PAGO = 'G' AND COD_SUCU = 999 ) OR (FOR_PAGO = 'C' AND COD_SUCU <> 999 ) OR (FOR_PAGO IS NULL )) AND  TIP_ENTI = 'BAN' AND IND_ACTI = 'A'
