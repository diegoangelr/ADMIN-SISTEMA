SELECT
M.COD_EMPL CEDULA,
M.NRO_CONT CONTR,
M.FEC_PAGO FECHAPAGO,
M.VAL_PAGA VALORPAGADO,
c.IND_acti IND_aCTI,
C.COD_CARG CARGCONTRATO,
N.COD_EMPL CEDULAPRENOMINA,
N.can_acum CANTPRENOMINA,
N.VAL_ACUM VALORPRENO
from nm_MBONI M
LEFT JOIN NM_PRENO N ON N.COD_EMPL = M.COD_EMPL AND N.cod_conc = 1100
LEFT JOIN NM_CONTR C ON C.COD_EMPL = M.COD_EMPL AND C.NRO_CONT = M.NRO_CONT
WHERE M.FEC_PAGO = '31/08/2021'

