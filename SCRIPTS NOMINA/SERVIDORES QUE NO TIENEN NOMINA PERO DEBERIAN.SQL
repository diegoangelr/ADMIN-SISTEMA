SELECT NM_CONTR.COD_EMPL,NM_CONTR.IND_ACTI,NM_CONTR.FEC_DEJA,NOM_AREA FROM NM_CONTR,SO_AREAS,BI_EMPLE WHERE
NM_CONTR.COD_EMPL=BI_EMPLE.COD_EMPL AND
NM_CONTR.COD_AREA=SO_AREAS.COD_AREA AND
((IND_ACTI='A') AND (NM_CONTR.COD_EMPL NOT IN(SELECT DISTINCT COD_EMPL FROM NM_preno WHERE (FEC_ACUM>='01/11/2021' AND FEC_ACUM<='30/11/2021' and tip_nomi = 'N') )
AND NM_CONTR.COD_EMPL NOT IN (SELECT COD_EMPL FROM NM_DIASN
WHERE
(
(FEC_DESD >= '01/11/2021' AND FEC_DESD <= '30/11/2021')
OR (FEC_HAST >= '01/11/2021' AND FEC_HAST <= '30/11/2021')
OR (FEC_DESD <= '30/11/2021' AND FEC_HAST >= '30/11/2021')
))))
ORDER BY COD_EMPL